#include <stdio.h>
int main()
{
    printf("Uppercase letters:\n");
    printf("A: %d\n", 'A');
    printf("B: %d\n", 'B');
    printf("C: %d\n", 'C');

    printf("\nLowercase letters:\n");
    printf("a: %d\n", 'a');
    printf("b: %d\n", 'b');
    printf("c: %d\n", 'c');

    printf("\nDigits:\n");
    printf("0: %d\n", '0');
    printf("1: %d\n", '1');
    printf("2: %d\n", '2');

    printf("\nSpecial symbols:\n");
    printf("$: %d\n", '$');
    printf("*: %d\n", '*');
    printf("+: %d\n", '+');
    printf("/: %d\n", '/');

    printf("\nBlank character:\n");
    printf("Blank: %d\n", ' ');
}
